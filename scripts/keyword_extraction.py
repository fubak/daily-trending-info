def extract_keywords(title: str) -> List[str]:
    """Extract meaningful keywords from a trend title (stop-word filter + tokenise)."""
    text = re.sub(r"[^\w\s]", " ", title.lower())
    words = text.split()
    keywords = [
        word
        for word in words
        if word not in _STOP_WORDS and len(word) > 2 and not word.isdigit()
    ]
    return keywords[:5]
